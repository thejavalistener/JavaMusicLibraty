package app.mapping;

public class Artist
{
	private String spotifyId;
	private String name;
	public String getSpotifyId()
	{
		return spotifyId;
	}
	public void setSpotifyId(String spotifyId)
	{
		this.spotifyId=spotifyId;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name=name;
	}
}
